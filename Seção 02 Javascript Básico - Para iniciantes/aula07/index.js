// Regras básicas das Constantes !!!
// Não podemos criar constantes com palavras reservadas.
// Constantes precisam ter nomes significativos.
// Não podemos começar o nome de uma constante com número.
// Utilizamos camelCase ( letras maiúsculas e minúscula, sem espaços).
// Case-sensitive.
// Não pode modificar o valor de uma constante.
// Não utilize VAR, utilize CONST !!!

const nome = 'Anderson'
console.log(nome)

const primeiroNome = 5
const segundoNome = 10
const resultado = primeiroNome * segundoNome
const resultadoDuplicado = resultado * 2

console.log(resultado)
console.log(resultadoDuplicado)

console.log(typeof primeiroNome)
