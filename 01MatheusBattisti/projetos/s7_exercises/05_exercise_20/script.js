function youCanGoToDrivingSchool(age) {
  if(age >= 18) {
    console.log(`You can enroll in the driving school.`);
  } else {
    console.log(`Unable to enroll in driving school.`);
  }
}

youCanGoToDrivingSchool(16);
youCanGoToDrivingSchool(25);
youCanGoToDrivingSchool(17);
youCanGoToDrivingSchool(18);