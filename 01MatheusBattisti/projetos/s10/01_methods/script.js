const calculator = {
  sum: function(a, b) { 
    return a + b
  },
  subtract: function(a, b) {
    return a - b
  }, 
  share: function(a, b) {
    return a / b
  },
  multiply: function(a, b) {
    return a * b
  }
 }

 console.log(calculator.sum(3, 4))
 console.log(calculator.subtract(9, 5))
 console.log(calculator.share(6, 2))
 console.log(calculator.multiply(4, 4))